class IdFormData {
  final String name;
  final String height;
  final String weight;
  final String hairColor;
  final String eyeColor;
  final String sex;
  final String city;
  final String address;

  const IdFormData({
    required this.name,
    required this.height,
    required this.weight,
    required this.hairColor,
    required this.eyeColor,
    required this.sex,
    required this.city,
    required this.address,
  });
}